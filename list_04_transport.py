# first list 第一个列表,使用方括号，元素之间用逗号分割。是有序的集合。列表（list）由一系列按特定顺序排列的元素组成。
motocycles = ['honda', 'yamaha','suzuki']
print(motocycles)
motocycles[0] = 'ducti'
print(motocycles)


motocycles = ['honda', 'yamaha','suzuki']
print(motocycles)
motocycles.append('ducti')
print(motocycles)


